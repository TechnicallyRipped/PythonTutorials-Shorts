


import numpy as np

x = np.array([1,2,0,4,3])

min_value = x.min()
max_value = x.max()


print(f'min value -> {min_value}')
print(f'max value -> {max_value}')


min_index = np.argmin(x)
max_index = np.argmax(x)


print(f'min_index -> {min_index}')
print(f'max_index -> {max_index}')


































